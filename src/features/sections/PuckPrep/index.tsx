import { PureComponent } from "react";

export class PuckPrep extends PureComponent {
  render() {
    return (
      <div>
        <ol>
          <li>
            Remove any leftover coffee from the portafilter: Before you start,
            make sure to remove any leftover coffee or grounds from the
            portafilter.
          </li>

          <li>
            Add the correct amount of ground coffee: Using a scale, measure out
            the correct amount of ground coffee for your espresso machine. A
            good rule of thumb is to use around 7-9 grams of coffee per shot.
          </li>

          <li>
            Distribute the coffee evenly: Use a distribution tool or a spoon to
            distribute the coffee evenly in the portafilter. This will help
            ensure an even extraction and a consistent shot.
          </li>

          <li>
            Tamp the coffee: Use a tamper to firmly press down on the coffee,
            making sure to apply even pressure across the surface of the coffee.
            This will help ensure that the water flows evenly through the coffee
            during the brewing process.
          </li>

          <li>
            Check the level of the puck: After tamping, check the level of the
            puck. The surface should be smooth and even, with no signs of
            cracking or unevenness.
          </li>

          <li>
            Wipe the rim of the portafilter: Before brewing, make sure to wipe
            the rim of the portafilter with a clean, damp cloth. This will help
            ensure a good seal between the portafilter and the espresso machine.
          </li>

          <li>
            If you have a paper or mesh filter, you can place one at the top of
            the shot. It will help keep your espresso machine clean and aid in
            more even water distribution.
          </li>
        </ol>
      </div>
    );
  }
}

export default PuckPrep;
