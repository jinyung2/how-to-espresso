import { PureComponent } from "react";

export class PullingShot extends PureComponent {
  render() {
    return (
      <div>
        <p>
          This step will widely vary depending on the type of espresso machine
          you have. Here are some general guidelines:
        </p>

        <ul>
          <li>
            Preheat your equipment: Before pulling your shot, make sure to
            preheat your espresso machine, portafilter, and cups. This will help
            ensure a consistent shot and prevent temperature fluctuations.
          </li>

          <li>
            Check your grind and dose: Make sure that your coffee is ground to
            the correct fineness and that you are using the correct amount of
            coffee. The ideal dose is around 7-9 grams per shot, but this may
            vary depending on your machine.
          </li>

          <li>
            Start with fresh, cold water: Use fresh, cold water to fill the
            water tank of your espresso machine. This will help ensure a clean
            and crisp taste.
          </li>

          <li>
            Start the extraction: Lock the portafilter into the group head and
            start the extraction. The ideal extraction time is around 25-30
            seconds for a double shot, but this may vary depending on your
            machine and the coffee you are using.
          </li>

          <li>
            Watch the pour: As the espresso is extracted, watch the pour and
            adjust the grind or dose if necessary. The ideal pour should be a
            thick, honey-like consistency with a crema on top.
          </li>

          <li>
            Stop the extraction: Stop the extraction once you have extracted the
            desired amount of coffee. Aim for around 1.5-2 ounces of espresso
            for a double shot.
          </li>
        </ul>
      </div>
    );
  }
}

export default PullingShot;
